    public void createTable(Node nodes[], String nome, File routerF){
        
        try {
            int i = 0;
           
            String dest, gateway, genmask, metric, iface;
                    
            Scanner scanner = new Scanner(routerF);

            while(nodes[i] != null){
                
                if(nodes[i].getTipo() == 3 && nodes[i].getNome().equals(nome)) break;
            
                i++;
                
            }
            
            while(scanner.hasNext()){
                        
                if(scanner.next().equals("Node:") && scanner.next().equals(Integer.toString(i))){
                    
                    while(scanner.hasNext()){
                        
                        if(scanner.next().equals("Priority:") && scanner.next().equals("-10")){
                            scanner.nextLine();
                            scanner.nextLine();
                            
                            int j = 0;
                            while(scanner.hasNext()){
                                 
                                dest = scanner.next();
                                gateway = scanner.next();
                                genmask = scanner.next();
                                scanner.next();
                                metric = scanner.next();
                                scanner.next();
                                scanner.next();
                                iface = scanner.next();  
                                                               
                                
                                if(!dest.equals("Node:")) {
                                    lista.add(new Router(dest, gateway, genmask, metric, iface));
                                }
 
                                else {
                                    while(scanner.hasNext())
                                        scanner.nextLine();
                                }
                                   
                                
                                j++;
                            }
                            
                            //System.out.print(scanner.nextLine());
                        }                        
                    }
                }
            }
            
        
             
        } catch (FileNotFoundException ex) {
            Logger.getLogger(RouterTable.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        model = new Table(lista);
        //atribui o modelo Ã  tabela
        jTable1.setModel(model);
    
    }
